#include<lpc21xx.h>
#include "header.h"

#define lcd_pins 0xff<<2
#define rs 1<<10
#define en 1<<11

unsigned char cgram_lut[]={
    0x00,0x00,0x00,0x0c,0x0c,0x00,0x00,0x00,
    0x00,0x06,0x06,0x00,0x0c,0x0c,0x00,0x00,
    0x03,0x03,0x00,0x06,0x06,0x00,0x0c,0x0c,
    0x00,0x1b,0x1b,0x00,0x00,0x1b,0x1b,0x00,
    0x1b,0x1b,0x00,0x0c,0x0c,0x00,0x1b,0x1b,
	0x1b,0x1b,0x00,0x1b,0x1b,0x00,0x1b,0x1b
};


void lcd_init(void){
	IODIR0|= lcd_pins|rs|en;
	cgram_write();
	lcd_cmd(0x01);
	lcd_cmd(0x38);
	lcd_cmd(0x06);
	lcd_cmd(0x0c);
}

void lcd_cmd(unsigned char cmd){
	IOCLR0=lcd_pins;
	IOSET0=cmd<<2;
	IOCLR0=rs;
	IOSET0=en;
	msdelay(2);
	IOCLR0=en;
}

void lcd_data(unsigned char data){
	IOCLR0=lcd_pins;
	IOSET0=data<<2;
	IOSET0=rs;
	IOSET0=en;
	msdelay(2);
	IOCLR0=en;
}

void lcd_str(char* s){
	while(*s)
		lcd_data(*s++);
}

void cgram_write(void){
	unsigned int i;
	lcd_cmd(0x40);
	for(i=0;i<48;i++)
		lcd_data(cgram_lut[i]);
		
}
